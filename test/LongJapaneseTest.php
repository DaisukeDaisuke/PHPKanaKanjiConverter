	{
		self::$converter = new PHPKanaKanjiConverter();
		// 必要ならここでユーザー辞書を登録する
		// e.g. self::$converter->registerUserDict('test', $dict);
	}

	#[DataProvider('longExactProvider')]
	public function testLongExactMatch(string $hiragana, string $expected) : void
	{
		$result = self::$converter->convert($hiragana, false, 1);

		$this->assertSame(
			$expected,
			$result['best']['text'],
			"\nInput:\n{$hiragana}\n\nExpected:\n{$expected}\n\nGot:\n{$result['best']['text']}\n"
		);
	}

